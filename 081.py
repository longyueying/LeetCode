class Solution:

    def findPivot(self, nums):
        begin = 0
        end = len(nums)-1

        while end - begin > 1:
            mid = int((begin+end)/2)
            if nums[mid] > nums[end]:
                begin = mid
            elif nums[mid] < nums[begin]:
                end = mid
            elif nums[mid] == nums[end] and nums[mid] > nums[mid+1]:
                begin = mid
            elif nums[mid] == nums[begin] and nums[mid] < nums[mid-1]:
                end = mid
            else:
                begin += 1
        return begin

    def binarySearch(self, nums, target):
        begin = 0
        end = len(nums)-1

        while begin <= end:
            mid = int((end+begin)/2)
            if nums[mid] == target:
                return True
            elif nums[mid] > target:
                end = mid-1
            else:
                begin = mid+1

        return False
    def search(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: bool
        """
        pivot = self.findPivot(nums)
        return self.binarySearch(nums[0:pivot+1],target) or self.binarySearch(nums[pivot+1:], target)

